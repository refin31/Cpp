#include <iostream>

using namespace std;

int main() {
	
	// Assignment
	int num = 10;
	
	// num = num + 5;
	num += 5; // 15
	cout << num << "\n";
	
	// num = num - 5;
	num -= 5; // 10
	cout << num << "\n";
	
	// num = num * 2;
	num *= 2; // 20
	cout << num << "\n";
	
	// num = num / 4;
	num /= 4; // 5
	cout << num << "\n";
	
	// num = num % 3;
	num %= 3; // 2
	cout << num << "\n";
	
	return 0;
}